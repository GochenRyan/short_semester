package com.dept.pojo;

import java.util.Date;

public class DeptTransfer{
	private int empno;
	private int oldDptno;
	private int newDptno;
	private String dptTransferType;
	private String dptTransferCause;
	private String dptTransferRemark;
	private Date dptTransferDate;
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public int getOldDptno() {
		return oldDptno;
	}
	public void setOldDptno(int oldDptno) {
		this.oldDptno = oldDptno;
	}
	public int getNewDptno() {
		return newDptno;
	}
	public void setNewDptno(int newDptno) {
		this.newDptno = newDptno;
	}
	public String getDptTransferType() {
		return dptTransferType;
	}
	public void setDptTransferType(String dptTransferType) {
		this.dptTransferType = dptTransferType;
	}
	public String getDptTransferCause() {
		return dptTransferCause;
	}
	public void setDptTransferCause(String dptTransferCause) {
		this.dptTransferCause = dptTransferCause;
	}
	public String getDptTransferRemark() {
		return dptTransferRemark;
	}
	public void setDptTransferRemark(String dptTransferRemark) {
		this.dptTransferRemark = dptTransferRemark;
	}
	public Date getDptTransferDate() {
		return dptTransferDate;
	}
	public void setDptTransferDate(Date dptTransferDate) {
		this.dptTransferDate = dptTransferDate;
	}
	
}
