package com.pro.pojo;

import java.util.Date;

public class Pro {
	private int empno;
	private String  ename;
	private String dname;
	private String pname;
	private String pro_state;
	private Date ProStartDate;
	private Date ProEndDate;
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPro_state() {
		return pro_state;
	}
	public void setPro_state(String pro_state) {
		this.pro_state = pro_state;
	}
	public Date getProStartDate() {
		return ProStartDate;
	}
	public void setProStartDate(Date proStartDate) {
		ProStartDate = proStartDate;
	}
	public Date getProEndDate() {
		return ProEndDate;
	}
	public void setProEndDate(Date proEndDate) {
		ProEndDate = proEndDate;
	}
	

}
